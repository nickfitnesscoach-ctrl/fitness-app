from django.apps import AppConfig


class AiConfig(AppConfig):
    """
    apps.py — “паспорт” приложения для Django.

    Простыми словами:
    - Django собирает проект из отдельных “приложений” (apps).
    - Этот файл говорит Django: “в проекте есть модуль AI распознавания еды”.

    Что тут важно:
    - name = путь к приложению (как папка/модуль в коде)
    - verbose_name = красивое название (может отображаться в админке/логах)
    """

    # Django будет использовать BigAutoField для авто-ID по умолчанию (если где-то появятся модели).
    default_auto_field = "django.db.models.BigAutoField"

    # Внутреннее имя приложения (путь к модулю).
    name = "apps.ai"

    # Человеческое имя, чтобы сразу было понятно, что это.
    verbose_name = "AI распознавание еды"
